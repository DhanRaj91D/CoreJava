public class Findvowels {
	
	private String vowels;
	

	public Findvowels(String v) {
		vowels=v; 
	}
	
	public String getVowels() {
		return vowels;
	}


	public void setVowels(String vowels) {
		this.vowels = vowels;
	}

	public String toString() {
		String str= "" + vowels + "";
		
		return str;
	}

	
}
